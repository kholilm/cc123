<script>
    function addTab(title, url) {
        if ($('#tt').tabs('exists', title)) {
            $('#tt').tabs('select', title);
        } else {
            var content = '<iframe id="frame" scrolling="auto" frameborder="0"  src="' + url + '" style="width:100%;height:100%;"></iframe>';
            $('#tt').tabs('add', {
                title: title,
                content: content,
                iconCls: 'icon-datagrid',
                closable: true,
                extractor: function(data) {
                    var tmp = $('<div></div>').html(data);
                    data = tmp.find('#content').html();
                    tmp.remove();
                    return data;
                }
            });
        }
    }
